public class TablaMultiplicacion {
    public static void main(String[] args) {
        for (int counter = 0; counter <= 10; counter++) {
            for (int multiplo = 0; multiplo <= 10; multiplo++) {
                System.out.println(counter * multiplo);
                System.out.println(" ");
            }
            System.out.println();
        }
    }
}
